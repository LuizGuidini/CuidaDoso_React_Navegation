// services/notificacaoService.js
import * as Notifications from 'expo-notifications';

export const agendarNotificacao = async ({ titulo, tipo }) => {
  await Notifications.scheduleNotificationAsync({
    content: {
      title: `Lembrete: ${tipo}`,
      body: titulo,
    },
    trigger: { seconds: 10 }, // funciona no Expo Go
  });
};
